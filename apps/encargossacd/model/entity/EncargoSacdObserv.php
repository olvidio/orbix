<?php

namespace encargossacd\model\entity;

use core\ClasePropiedades;
use core\DatosCampo;
use core\Set;

/**
 * Fitxer amb la Classe que accedeix a la taula encargo_sacd_observ
 *
 * @package orbix
 * @subpackage model
 * @author Daniel Serrabou
